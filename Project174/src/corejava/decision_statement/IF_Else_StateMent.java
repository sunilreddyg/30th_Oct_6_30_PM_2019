package corejava.decision_statement;

public class IF_Else_StateMent {

	public static void main(String[] args)
	{
		
		/*
		 * IF-Else:--> 
		 * 		=> Any two decisions to execute
		 * 		=> If condition execute only when boolean value return true.
		 * 		=> else condition execute when boolean value return false.
		 */
		
		
		int Age=17;
		if(Age > 18)
		{
			System.out.println("true-condition");
		}
		else
		{
			System.out.println("false-condition");
		}
		
		
		
		//Two Decision statement with single condition
		if(Age > 16)
			System.out.println("true-condition");
		else
			System.out.println("false-condition");
		
		

	}

}
